    public function comparisonPhoneList($arr, $type)
    {
        $appList = $this->getExplodeList($type);
        $appList = StringHelper::clearSpaceToLower($appList);

        $arr = StringHelper::clearSpaceToLower($arr);

        if (is_array($arr)) {
            return array_unique(array_intersect($arr, $appList));
        }
        return in_array($arr, $appList);
    }

    public function getExplodeList($type)
    {
        switch ($type) {
            case self::TYPE_INSTANT_LOAN:
                $listStr = self::INSTANT_LOAN_LIST;
                break;
            case self::TYPE_PERSONAL:
                $listStr = self::PERSONAL_LIST;
                break;
            case self::TYPE_FINANCE:
                $listStr = self::FINANCE_LIST;
                break;
            case self::TYPE_COLLECTION:
                $listStr = self::COLLECTION_LIST;
                break;
            case self::TYPE_LOANAPP:
                $listStr = self::LOANAPP_LIST;
                break;
            default:
                return [];
        }

        return array_filter(explode(PHP_EOL, $listStr));
    }
